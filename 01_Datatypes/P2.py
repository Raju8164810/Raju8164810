# Number Systems
# binary 
# base is 2 ( 0, 1)
# prefix is 0b

# hexa 
# base is 16 ( a to f and 0 to 9)
# prefix 0X

# octa 
# base is 0 ( 0 to 7)
# prefix is 0

binary = 0b010101
print(binary)

hexadecimal = 0xace
print(hexadecimal)

octal = 0o123 
print(octal)

# build in functions
b1 = bin(10)
print(b1)

b2 = oct(10)
print(b2)

b3 = hex(10)
print(b3)
