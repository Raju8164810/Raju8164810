# sort()
l1 = [50, 20, 60, 10, 40, 30]
print(l1)  # [50, 20, 60, 10, 40, 30]
l1.sort(reverse=False)
print(l1) # [10, 20, 30, 40, 50, 60]
l1.sort(reverse=True)
print(l1) # [60, 50, 40, 30, 20, 10]

